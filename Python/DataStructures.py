# Lists -> These are mutable
numbers = [1, 2, 3, 4]
numbers.append(5)

print(numbers)

# Tuples -> These are immutable
coordinates = (10, 20)
print(coordinates)

# Sets -> Unique values
unique_numbers = {1, 2, 2, 3, 4}
print(unique_numbers)

# Dictionaries -> Key-value pairs 
student = {
    "name": "Vedant",
    "age": 21,
    "course": "Python"
}

print(student["name"])
