# Variables
x = 10
name = "Vedant"
pi = 3.14

print(x)
print(name)
print(pi)

# Constants 
PI = 3.14159
GRAVITY = 9.8

print(PI)
print(GRAVITY)

# Data types 
# Integers
a = 10
b = -5
c = 100

print(a + b)
print(type(a))

# Strings (F-Strings)
name = "Vedant"
age = 21

message = f"My name is {name} and I am {age} years old."
print(message)

# Boolean
is_python_fun = True
is_raining = False

print(is_python_fun)
print(type(is_raining))

# Type conversions 
x = "10"
y = int(x)

z = 3.5
w = int(z)

print(y)
print(w)
